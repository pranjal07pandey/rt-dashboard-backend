    Complete Your Profile
        Complete your profile with your name and company details. This allows us to contact you to help you set up Record Time for your business.
        The company logo, name and address you enter on your profile will be used when you create your dockets and invoices. You can upload .jpeg, .jpg and .png files for your logo.

    Employee Management
        Go to “Employee management” from the menu and click “Manage Employees”.
        Add your employees. Your employees will not have access to the backend system unless you make them an admin.
        Once you add your employees, they will receive an email with instructions together with login details to access the app.

    Docket Book Manager
        Go to “Docket Book Manager” from the menu and click “Docket Templates”.
        You will find some default templates you can assign to your employees. These can be edited to suit your needs. You can also create your own templates.
        Go to “Docket Book Manager” from the menu and click “Assign Dockets Template” to assign/tag templates for your employees to use.

